# All capital letters
print('Hello, Wold.'.upper())

# All lowercase letters
print('Hello, Wold.'.lower())

# Capitalize letters
print('Hello, Wold.'.capitalize())

# Capilize all first letters
print('Hello, Wold.'.title())

# Swap the capitalized letters
print('Hello, Wold.'.swapcase())

# similar to lower but it
# lowercase other languages
print('Hello, Wold.'.casefold())

# you can concat literal strings like this
s3 = 'this string' ' that string'
print(s3)

